"""Judge LLM para evaluar calidad de alertas generadas por el pipeline."""

from dataclasses import dataclass
from typing import Any, Dict, List, Optional

from pydantic import BaseModel

from config import LLMConfig
from pipeline_benchmark import GeneratedAlertInfo


class JudgeVerdict(BaseModel):
    score: float
    explanation: str


@dataclass
class JudgeResult:
    score: float = 0.0
    explanation: str = ""
    error: Optional[str] = None


class PipelineJudge:
    def __init__(self, cfg: LLMConfig):
        self.cfg = cfg
        self.client = None
        if cfg:
            from common.llm_client_factory import create_instructor_client, ModelConfig

            mc = ModelConfig(
                name=cfg.model_name,
                provider=cfg.provider,
                base_url=cfg.base_url,
                api_key=cfg.api_key,
                max_retries=cfg.max_retries,
                timeout=cfg.timeout,
            )
            try:
                self.client, self.mode = create_instructor_client(mc)
            except Exception as e:
                print(f"  WARNING: judge client: {e}")

    def judge_test_case(
        self,
        tc_id: str,
        instruction: str,
        expected_alerts: Dict[str, Any],
        generated_alerts: List[GeneratedAlertInfo],
    ) -> JudgeResult:
        if self.client is None:
            return JudgeResult()

        prompt = f"""You evaluate an integrated ATC alert pipeline.

TEST: {tc_id}
INSTRUCTION: "{instruction}"

EXPECTED ALERTS (ground truth):
{self._fmt_expected(expected_alerts)}

ACTUAL ALERTS GENERATED BY PIPELINE:
{self._fmt_generated(generated_alerts)}

Score 0.0-1.0 considering:
- Were expected violations detected? (recall)
- Were there false alarms on clean/compliant cases? (precision)
- Were severity levels correct?
- Overall coherence of the alert set

Respond with JSON: {{"score": 0.0-1.0, "explanation": "brief reason"}}"""

        try:
            from common.llm_client_factory import ModelConfig

            mc = ModelConfig(
                name=self.cfg.model_name,
                provider=self.cfg.provider,
                base_url=self.cfg.base_url,
                api_key=self.cfg.api_key,
                max_retries=self.cfg.max_retries,
                timeout=self.cfg.timeout,
            )
            resp = self.client.chat.completions.create(
                response_model=JudgeVerdict,
                messages=[{"role": "user", "content": prompt}],
                temperature=0.1,
                max_tokens=256,
                **mc.completion_kwargs(),
            )
            return JudgeResult(score=float(resp.score), explanation=resp.explanation)
        except Exception as e:
            return JudgeResult(score=0.5, explanation="", error=str(e))

    @staticmethod
    def _fmt_expected(alerts: Dict[str, Any]) -> str:
        if not alerts:
            return "  No alerts expected (clean/compliant scenario)"
        lines = []
        for rid, exp in alerts.items():
            status = "SAFE" if exp.satisfied else "VIOLATION"
            sev = f" ({exp.severity})" if exp.severity else ""
            lines.append(f"  {rid}: {status}{sev}")
        return "\n".join(lines)

    @staticmethod
    def _fmt_generated(alerts: List[GeneratedAlertInfo]) -> str:
        if not alerts:
            return "  No alerts generated"
        lines = []
        for alert in alerts:
            sev = f" ({alert.severity})" if alert.severity else ""
            lines.append(f"  {alert.rule_id}: VIOLATION{sev} — {alert.explanation[:120]}")
        return "\n".join(lines)


def run_judge_all(
    judge: PipelineJudge,
    test_cases,
    results: List,
) -> Dict[str, float]:
    if judge is None or judge.client is None:
        return {}
    scores: Dict[str, float] = {}
    for tc in test_cases:
        bench = next((r for r in results if r.test_case_id == tc.id), None)
        if bench is None:
            continue
        res = judge.judge_test_case(
            tc.id, tc.instruction, tc.expected_alerts, bench.generated_alerts
        )
        scores[tc.id] = res.score
    return scores
